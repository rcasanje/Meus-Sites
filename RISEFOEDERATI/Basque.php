<h1>History</h1>
			 <h5>Historians believe the Basque identity exists in Europe for more than 7.000 years. The name "Basque" comes from the latin "Vascones", and evolved later on to today's "Basques". Romans knew of the existance of the Basques since the 1st century BC, being one of the largest tribes inhabiting the region of modern Spain and Portugal, together with other tribes like the Cantabrians and the Asturians. The Cantabrian Wars, however, decimated all indo-iberian tribes; with the exception of the Basques; most of the survivors of such tribes were assimilated by them later on.

Romans never fully managed to control their territories. Whenever wars were waged against them, the Basques retreated to the Pyren√©es region and successfully defended their more inhospitable places. This led to the creation of many cities which were Roman only in paper; the Basques retained control whenever official command was attempted. The situation was increasingly worsened with the fall of the Empire, starting during the 2nd century AD. The migrations of tribes in the 4th century brought more problems to the Basques than the Romans themselves, however; roaming raiding tribes constantly threatened their territories, but they too were not successful in settling in the regions in their control. Knowing that their survival would have to go hand-to-hand with guerrilla tactics, the Basques used a very successful military tactic; calling the Bagaudae. 
</h5>

<h5>A Bagauda was the Latin term for a brigand, an umbrella term for any military force more organized than bandits, but less organized than an army. The Basques employed their Bagaudae as their main military force, mostly composed of freemen with some military experience and the clan warriors with most prestige as leaders. The Romans usually attempted to dissuade local population with propaganda, calling them nothing but rebel peasants. Though most Romans would think that, the core of the movements was funded and idealized by the Basque tribal leaders. They would enforce Basque dominion in the cities near their territories, blackmailing Roman politicians and officers. Any attempt to subvert them was swiftly responded with raids in villas, cities or even assassination. The Basques never needed to claim their territory as their own; they only needed cities that would serve as their bases of operation while the Romans were not looking their way.

Through this method, the Basques archieved relative stability when compared to other roaming tribes, and even adopted several Roman military tactics in bigger battles. The Romans who lived in Basque-controlled territory would even use this system to their advantage; during the years of Rise of the Foederati, one of the most successful Bagaudae leaders was a Basco-Roman general named Basilius. His military and political prowess was deemed primordial for the survival of the tribe during the 5th century, making alliances with the Suebi to consolidate their presence in parts of the Hispania province not conquered by the Visigoths nor Romans.
</h5>
